package basics;

public class mathoperattor {
    public static void main(String[] args) {
        int a = 15;
        int b = 3;
        int sum = a+b;
        int div = a/b;
        int sub = a-b;
        int mul = a*b;
        int mod = a%b;
        System.out.println("sum  "+sum);
        System.out.println("sub  "+sub);
        System.out.println("mul  "+mul);
        System.out.println("div  "+div);
        System.out.println("mod  "+mod);
    }
}
